package org.vi.bean.user;

public class TeaUser {

	private int tea_id;
	
	private String tea_loginName;
	
	private String tea_password;
	
	private String tea_name;
	
	private String tea_major;

	/**
	 * @return the tea_id
	 */
	public int getTea_id() {
		return tea_id;
	}

	/**
	 * @param tea_id the tea_id to set
	 */
	public void setTea_id(int tea_id) {
		this.tea_id = tea_id;
	}

	/**
	 * @return the tea_loginName
	 */
	public String getTea_loginName() {
		return tea_loginName;
	}

	/**
	 * @param tea_loginName the tea_loginName to set
	 */
	public void setTea_loginName(String tea_loginName) {
		this.tea_loginName = tea_loginName;
	}

	/**
	 * @return the tea_password
	 */
	public String getTea_password() {
		return tea_password;
	}

	/**
	 * @param tea_password the tea_password to set
	 */
	public void setTea_password(String tea_password) {
		this.tea_password = tea_password;
	}

	/**
	 * @return the tea_name
	 */
	public String getTea_name() {
		return tea_name;
	}

	/**
	 * @param tea_name the tea_name to set
	 */
	public void setTea_name(String tea_name) {
		this.tea_name = tea_name;
	}

	/**
	 * @return the tea_major
	 */
	public String getTea_major() {
		return tea_major;
	}

	/**
	 * @param tea_major the tea_major to set
	 */
	public void setTea_major(String tea_major) {
		this.tea_major = tea_major;
	}
	
}
