package org.vi.bean.user;

public class StuUser {

	private int stu_id;
	
	private String stu_loginName;
	
	private String stu_password;
	
	private String stu_name;
	
	private String stu_term;
	
	private int stu_gen;
	
	private String stu_class;
	
	private String stu_major;

	/**
	 * @return the stu_id
	 */
	public int getStu_id() {
		return stu_id;
	}

	/**
	 * @param stu_id the stu_id to set
	 */
	public void setStu_id(int stu_id) {
		this.stu_id = stu_id;
	}

	/**
	 * @return the stu_loginName
	 */
	public String getStu_loginName() {
		return stu_loginName;
	}

	/**
	 * @param stu_loginName the stu_loginName to set
	 */
	public void setStu_loginName(String stu_loginName) {
		this.stu_loginName = stu_loginName;
	}

	/**
	 * @return the stu_name
	 */
	public String getStu_name() {
		return stu_name;
	}

	/**
	 * @param stu_name the stu_name to set
	 */
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}

	/**
	 * @return the stu_term
	 */
	public String getStu_term() {
		return stu_term;
	}

	/**
	 * @param stu_term the stu_term to set
	 */
	public void setStu_term(String stu_term) {
		this.stu_term = stu_term;
	}

	/**
	 * @return the stu_gen
	 */
	public int getStu_gen() {
		return stu_gen;
	}

	/**
	 * @param stu_gen the stu_gen to set
	 */
	public void setStu_gen(int stu_gen) {
		this.stu_gen = stu_gen;
	}

	/**
	 * @return the stu_class
	 */
	public String getStu_class() {
		return stu_class;
	}

	/**
	 * @param stu_class the stu_class to set
	 */
	public void setStu_class(String stu_class) {
		this.stu_class = stu_class;
	}

	/**
	 * @return the stu_major
	 */
	public String getStu_major() {
		return stu_major;
	}

	/**
	 * @param stu_major the stu_major to set
	 */
	public void setStu_major(String stu_major) {
		this.stu_major = stu_major;
	}

	/**
	 * @return the stu_password
	 */
	public String getStu_password() {
		return stu_password;
	}

	/**
	 * @param stu_password the stu_password to set
	 */
	public void setStu_password(String stu_password) {
		this.stu_password = stu_password;
	}
	
	
}
