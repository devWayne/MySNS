package org.vi.bean.note;

public class Note {

	 private int note_id;
	 
	 private String note_au; 
	 
	 private String note_content;

	/**
	 * @return the note_id
	 */
	public int getNote_id() {
		return note_id;
	}

	/**
	 * @param note_id the note_id to set
	 */
	public void setNote_id(int note_id) {
		this.note_id = note_id;
	}

	/**
	 * @return the note_au
	 */
	public String getNote_au() {
		return note_au;
	}

	/**
	 * @param note_au the note_au to set
	 */
	public void setNote_au(String note_au) {
		this.note_au = note_au;
	}

	/**
	 * @return the note_content
	 */
	public String getNote_content() {
		return note_content;
	}

	/**
	 * @param note_content the note_content to set
	 */
	public void setNote_content(String note_content) {
		this.note_content = note_content;
	}
	 
	 
}
