package org.vi.bean.notice;

public class Notice {

	 private int notice_id;
	 
	 private String notice_au;
	 
	 private String  notice_content;

	/**
	 * @return the notice_id
	 */
	public int getNotice_id() {
		return notice_id;
	}

	/**
	 * @param notice_id the notice_id to set
	 */
	public void setNotice_id(int notice_id) {
		this.notice_id = notice_id;
	}

	/**
	 * @return the notice_au
	 */
	public String getNotice_au() {
		return notice_au;
	}

	/**
	 * @param notice_au the notice_au to set
	 */
	public void setNotice_au(String notice_au) {
		this.notice_au = notice_au;
	}

	/**
	 * @return the notice_content
	 */
	public String getNotice_content() {
		return notice_content;
	}

	/**
	 * @param notice_content the notice_content to set
	 */
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	 
	 
}
