package org.vi.bean.reply;

public class Reply {

	  private int reply_id ;
	  
	  private String reply_type ;
	  
	  private String reply_au ;

	  private String reply_content ;

	/**
	 * @return the reply_id
	 */
	public int getReply_id() {
		return reply_id;
	}

	/**
	 * @param reply_id the reply_id to set
	 */
	public void setReply_id(int reply_id) {
		this.reply_id = reply_id;
	}

	/**
	 * @return the reply_type
	 */
	public String getReply_type() {
		return reply_type;
	}

	/**
	 * @param reply_type the reply_type to set
	 */
	public void setReply_type(String reply_type) {
		this.reply_type = reply_type;
	}

	/**
	 * @return the reply_au
	 */
	public String getReply_au() {
		return reply_au;
	}

	/**
	 * @param reply_au the reply_au to set
	 */
	public void setReply_au(String reply_au) {
		this.reply_au = reply_au;
	}

	/**
	 * @return the reply_content
	 */
	public String getReply_content() {
		return reply_content;
	}

	/**
	 * @param reply_content the reply_content to set
	 */
	public void setReply_content(String reply_content) {
		this.reply_content = reply_content;
	}
	  
	  
}
